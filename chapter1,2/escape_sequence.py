# 1. Single quote
print('It\'s a sunny day!')

# 2. Double quote
print("He said, \"Python is awesome!\"")

# 3. Backslash
print("This is a backslash: \\")

# 4. Newline
print("First line\nSecond line")

# 5. Horizontal tab
print("Column 1\tColumn 2")

# 6. Carriage return (moves to the start of the line and overwrites)
print("Hello, World!\rPython")

# 7. Backspace
print("Hello\b World")

# 8. Form feed (used in some printers, might not visibly affect output)
print("Text before\fForm feed")

# 9. Vertical tab (rarely used)
print("Line 1\vLine 2")

# 10. Null character
print("Null character here\0End of string")

# 11. Hexadecimal value
print("Hexadecimal A: \x41")

# 12. Octal value
print("Octal A: \101")

# 13. 16-bit Unicode character
print("Unicode 0041: \u0041")

# 14. 32-bit Unicode character
print("Unicode 00000041: \U00000041")
